            <h3 class="ttls">Petting zoos</h3>
            <p>Petting zoos feature domesticated animals that are gentle enough for children to pet and feed. Sheep, goats, donkeys, and rabbits are common petting zoo animals.

These types of zoos are found at parks and inside of larger zoos. Sometimes mobile petting zoos travel with fairs or carnivals from city to city. Some zoos are dedicated entirely to certain species. Aquariums are types of zoos that exclusively house aquatic animals. The Sydney Aquarium in Australia has exhibits of all of Australia’s major water systems and is home to more than 650 native Australian species.

Aviaries and bird parks are another type of specialized zoo. The Jurong Bird Park in Singapore has more than 8,000 birds of 600 species from around the world. Jurong has more than 1,000 flamingoes in an African wetlands exhibit that features a daily simulated thunderstorm.<br></p>
